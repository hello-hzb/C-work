

/***************************************************************
对变量地址进行计算的时候，和变量的数据类型有关
指令中对变量地址加1，实际上结果的增量大小为该变量的字节大小，如对int数据的地址+1，实际结果是该数据的地址+4
但是做加法或者减法前对地址做数据转为char *，那么这时候+1.结果就是+1
假设 a的地址为10：
	b = &a + 1;   //b=14
	
	b = (char *)(&a) + 1;  //b=11


****************************************************************/
void main()
{
	int a = 0;
	long b = 0;
	
	b = &a + 1;
	printf("b size is %x\n", b);

	b = (char *)(&a) + 1;
	printf("b size is %x\n", b);
}